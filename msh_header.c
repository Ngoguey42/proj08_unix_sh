static void	header1(int i)
{
	const char	h[][20] = {

	"\033[2m.___  ___. \033[0m",	" __  ",	".__   __. ",	" __  ",
	"\033[2m|   \\/   | \033[0m",	"|  | ",	"|  \\ |  | ",	"|  | ",
	"\033[2m|  \\  /  | \033[0m",	"|  | ",	"|   \\|  | ",	"|  | ",
	"\033[2m|  |\\/|  | \033[0m",	"|  | ",	"|  . `  | ",	"|  | ",
	"\033[2m|  |  |  | \033[0m",	"|  | ",	"|  |\\   | ",	"|  | ",
	"\033[2m|__|  |__| \033[0m",	"|__| ",	"|__| \\__| ",	"|__| "};

	ft_putstr(h[i]);
	return ;
}

static void	header2(int i)
{
	const char	h[][18] = {

	 "\033[2m     _______.", " __    __  \033[0m", " __ \n",
	 "\033[2m    /       |", "|  |  |  | \033[0m", "|  |\n",
	 "\033[2m   |   (----`", "|  |__|  | \033[0m", "|  |\n",
	 "\033[2m    \\   \\    ", "|   __   | \033[0m", "|  |\n",
	 "\033[2m.----)   |   ", "|  |  |  | \033[0m", "|__|\n",
	 "\033[2m|_______/    ", "|__|  |__| \033[0m", "(__)\n"};
	ft_putstr(h[i]);
	return ;
}

void		msh_header(void)
{
	int	i;

	i = 0;
	while (i < 7 * 6)
	{
		if (i % 7 < 4)
			header1(i / 7 * 4 + i % 7);
		else
			header2(i / 7 * 3 + i % 7 - 4);
		i++;
	}
	return ;
}
